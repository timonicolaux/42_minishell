#include "libft.h"

int	ft_cchr_str(char c, char *cmp)
{
	int		i;

	i = 0;
	if (c == '\0')
		return (0);
	while (cmp[i])
	{
		if (cmp[i] == c)
			return (1);
		i++;
	}
	return (0);
}
